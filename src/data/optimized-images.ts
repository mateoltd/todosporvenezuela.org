export type OptimizedImageName = keyof typeof optimizedImageFiles;
export type StaticOptimizedImageName = keyof typeof staticOptimizedImageFiles;

export const optimizedImageSrc = (
  name: OptimizedImageName,
  width: number,
  format: OptimizedImageFormat,
) => {
  const widths = optimizedImageFiles[name];
  const files = widths[width as keyof typeof widths];
  const file = files?.[format as keyof typeof files];

  if (!file) {
    throw new Error(`Missing optimized image variant: ${name}-${width}.${format}`);
  }

  return `/images/optimized/${file}`;
};

export const optimizedImageSrcSet = (
  name: OptimizedImageName,
  widths: readonly number[],
  format: OptimizedImageFormat,
) => widths.map((width) => `${optimizedImageSrc(name, width, format)} ${width}w`).join(", ");

export const staticOptimizedImageSrc = (
  name: StaticOptimizedImageName,
  format: OptimizedImageFormat,
) => {
  const files = staticOptimizedImageFiles[name];
  const file = files?.[format as keyof typeof files];

  if (!file) {
    throw new Error(`Missing static optimized image: ${name}.${format}`);
  }

  return `/images/optimized/${file}`;
};
